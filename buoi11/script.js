function tong(x, y) {
  alert("tổng: " + (x + y));
}
function toanbo(x, y) {
  alert("tổng: " + (x + y));
  alert("hiệu: " + (x - y));
  alert("nhân: " + x * y);
  alert("chia: " + x / y);
  alert("dư: " + (x % y));
  alert("mũ: " + x ** y);
}
function chuvi(x, y) {
  alert("chu vi: " + (x + y) * 2);
}
function dientich(x, y) {
  alert("diện tích: " + x * y);
}
function hinhtron(x) {
  alert("chu vi hình tròn: " + 2 * 3.14 * x);
  alert("diện tích hình tròn: " + 3.14 * x ** 2);
}
